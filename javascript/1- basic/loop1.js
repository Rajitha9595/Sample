console.log("For Loop Example");
/**
 * for(init; condition; ==/--)
 * {
 *      statement
 * }
 * 
 * ++ -> increase by 1
 * -- -> decrease by 1
 */

for(let i=0;i<=10;i++){

    console.log("The For say: ",i);
}

console.log("-----Desc order loop-----");


for(let i=10;i>=1;i--){

    console.log("The For say: ",i);
}

console.log("Increase value by 2");

for(let i=2000;i<=2023;i=i+2){
    console.log("The year is: ",i);
}
